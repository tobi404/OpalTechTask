import SwiftUI

extension Font {
    /// size: 12
    static func opalFootnote(_ weight: Weight? = nil) -> Font {
        .system(size: 12, weight: weight)
    }
    
    /// size: 12
    static func opalBody(_ weight: Weight? = nil) -> Font {
        .system(size: 16, weight: weight)
    }
}
